function interpolate(template: string, replacements: Replacements) {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(replacements[key] ?? `{${key}}`))
}

export function useAdminText() {
  const { locale } = useLocale()

  return useCallback((arabic: string, replacements: Replacements = {}) => {
    const text = locale === 'ar' ? arabic : adminTranslations[arabic]?.[locale] || arabic
    return interpolate(text, replacements)
  }, [locale])
}
